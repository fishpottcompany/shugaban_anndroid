package com.shugaban.shugaban.Models;

public class MovieModel {
    private long rowId;
    private int movie_id;
    private String movie_name;
    private String movie_year;
    private String movie_plot;
    private String movie_category;
    private String movie_duration;
    private String movie_portrait;
    private String movie_landscape;
    private String movie_description;
    private String movie_trailer_link;
    private String movie_amount_naira;
    private String movie_amount_dollar;

    public long getRowId() {
        return rowId;
    }

    public void setRowId(long rowId) {
        this.rowId = rowId;
    }

    public int getMovie_id() {
        return movie_id;
    }

    public void setMovie_id(int movie_id) {
        this.movie_id = movie_id;
    }

    public String getMovie_name() {
        return movie_name;
    }

    public void setMovie_name(String movie_name) {
        this.movie_name = movie_name;
    }

    public String getMovie_year() {
        return movie_year;
    }

    public void setMovie_year(String movie_year) {
        this.movie_year = movie_year;
    }

    public String getMovie_category() {
        return movie_category;
    }

    public void setMovie_category(String movie_category) {
        this.movie_category = movie_category;
    }

    public String getMovie_plot() {
        return movie_plot;
    }

    public void setMovie_plot(String movie_plot) {
        this.movie_plot = movie_plot;
    }

    public String getMovie_duration() {
        return movie_duration;
    }

    public void setMovie_duration(String movie_duration) {
        this.movie_duration = movie_duration;
    }

    public String getMovie_portrait() {
        return movie_portrait;
    }

    public void setMovie_portrait(String movie_portrait) {
        this.movie_portrait = movie_portrait;
    }

    public String getMovie_landscape() {
        return movie_landscape;
    }

    public void setMovie_landscape(String movie_landscape) {
        this.movie_landscape = movie_landscape;
    }

    public String getMovie_description() {
        return movie_description;
    }

    public void setMovie_description(String movie_description) {
        this.movie_description = movie_description;
    }

    public String getMovie_trailer_link() {
        return movie_trailer_link;
    }

    public void setMovie_trailer_link(String movie_trailer_link) {
        this.movie_trailer_link = movie_trailer_link;
    }

    public String getMovie_amount_naira() {
        return movie_amount_naira;
    }

    public void setMovie_amount_naira(String movie_amount_naira) {
        this.movie_amount_naira = movie_amount_naira;
    }

    public String getMovie_amount_dollar() {
        return movie_amount_dollar;
    }

    public void setMovie_amount_dollar(String movie_amount_dollar) {
        this.movie_amount_dollar = movie_amount_dollar;
    }
}
